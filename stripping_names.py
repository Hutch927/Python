full_name = " fred\n\thutchinson "  #define full_name. Added in whitespace with a tab and newline
print(full_name)					#print the original string with whitespaces
full_name = full_name.title()		#permenatly gives the string 'full_name' title-case
print(full_name.rstrip())			#strips the whitespace on the right side of the string
print(full_name.lstrip())			#strips the whitespace on the left side of the string
print(full_name.strip())			#strips the whitespace on both sides of the string